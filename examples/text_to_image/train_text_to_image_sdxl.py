def group_by_keys_nothrow(data, keys=base_plus_ext, lcase=True, suffixes=None, handler=None):
    """Return function over iterator that groups key, value pairs into samples.

    :param keys: function that splits the key into key and extension (base_plus_ext)
    :param lcase: convert suffixes to lower case (Default value = True)
    """
    current_sample = None
    for filesample in data:
        assert isinstance(filesample, dict)
        fname, value = filesample["fname"], filesample["data"]
        prefix, suffix = keys(fname)
        if prefix is None:
            continue
        if lcase:
            suffix = suffix.lower()
        # FIXME webdataset version throws if suffix in current_sample, but we have a potential for
        #  this happening in the current LAION400m dataset if a tar ends with same prefix as the next
        #  begins, rare, but can happen since prefix aren't unique across tar files in that dataset
        if current_sample is None or prefix != current_sample["__key__"] or suffix in current_sample:
            if valid_sample(current_sample):
                yield current_sample
            current_sample = {"__key__": prefix, "__url__": filesample["__url__"]}
        if suffixes is None or suffix in suffixes:
            current_sample[suffix] = value
    if valid_sample(current_sample):
        yield current_sample


def tarfile_to_samples_nothrow(src, handler=wds.warn_and_continue):
    # NOTE this is a re-impl of the webdataset impl with group_by_keys that doesn't throw
    streams = url_opener(src, handler=handler)
    files = tar_file_expander(streams, handler=handler)
    samples = group_by_keys_nothrow(files, handler=handler)
    return samples


def make_random_irregular_mask(height, width, max_angle=4, max_len=60, max_width=20, min_times=0, max_times=10):
    mask = np.zeros((height, width), np.float32)

    times = np.random.randint(min_times, max_times + 1)

    for i in range(times):
        start_x = np.random.randint(width)
        start_y = np.random.randint(height)

        for j in range(1 + np.random.randint(5)):
            angle = 0.01 + np.random.randint(max_angle)

            if i % 2 == 0:
                angle = 2 * 3.1415926 - angle

            length = 10 + np.random.randint(max_len)

            brush_w = 5 + np.random.randint(max_width)

            end_x = np.clip((start_x + length * np.sin(angle)).astype(np.int32), 0, width)
            end_y = np.clip((start_y + length * np.cos(angle)).astype(np.int32), 0, height)

            choice = random.randint(0, 2)

            if choice == 0:
                cv2.line(mask, (start_x, start_y), (end_x, end_y), 1.0, brush_w)
            elif choice == 1:
                cv2.circle(mask, (start_x, start_y), radius=brush_w, color=1.0, thickness=-1)
            elif choice == 2:
                radius = brush_w // 2
                mask[start_y - radius : start_y + radius, start_x - radius : start_x + radius] = 1
            else:
                assert False

            start_x, start_y = end_x, end_y

    return mask


def make_random_rectangle_mask(
    height, width, margin=10, bbox_min_size=30, bbox_max_size=100, min_times=0, max_times=3
):
    mask = np.zeros((height, width), np.float32)

    bbox_max_size = min(bbox_max_size, height - margin * 2, width - margin * 2)

    times = np.random.randint(min_times, max_times + 1)

    for i in range(times):
        box_width = np.random.randint(bbox_min_size, bbox_max_size)
        box_height = np.random.randint(bbox_min_size, bbox_max_size)

        start_x = np.random.randint(margin, width - margin - box_width + 1)
        start_y = np.random.randint(margin, height - margin - box_height + 1)

        mask[start_y : start_y + box_height, start_x : start_x + box_width] = 1

    return mask


def make_wds_inpainting_dataset(
    wds_dataset_url: str,
    tokenizer_one,
    tokenizer_two,
    proportion_empty_prompts: float,
    per_gpu_batch_size: int,
    resolution: int = 256,
    min_resolution: Optional[int] = None,
    shuffle_buffer_size: int = 1000,
):
    crop_transform = transforms.RandomCrop(resolution)

    pixel_values_transform = transforms.Compose(
        [
            transforms.Resize(resolution, interpolation=transforms.InterpolationMode.BILINEAR),
            crop_transform,
            transforms.ToTensor(),
            transforms.Normalize([0.5], [0.5]),
        ]
    )

    def mapper(d):
        pixel_values = pixel_values_transform(d["image"])

        metadata = d["metadata"]

        original_width = int(metadata.get("original_width", 0.0))
        original_height = int(metadata.get("original_height", 0.0))

        crop_top_left_y, crop_top_left_x, crop_height, crop_width = crop_transform.get_params(
            d["image"], (resolution, resolution)
        )

        time_ids = torch.tensor(
            [original_width, original_height, crop_top_left_y, crop_top_left_x, resolution, resolution]
        )

        if random.random() < proportion_empty_prompts:
            text = ""
        else:
            text = d["text"]

        tokenzier_one_text_input_ids = tokenizer_one(
            text,
            padding="max_length",
            max_length=tokenizer_one.model_max_length,
            truncation=True,
            return_tensors="pt",
        ).input_ids[0]

        tokenzier_two_text_input_ids = tokenizer_two(
            text,
            padding="max_length",
            max_length=tokenizer_two.model_max_length,
            truncation=True,
            return_tensors="pt",
        ).input_ids[0]

        if random.random() < 0.5:
            mask = make_random_rectangle_mask(resolution, resolution)
        else:
            mask = make_random_irregular_mask(resolution, resolution)

        mask = mask[None, :, :].astype(np.int32)

        return {
            "mask": mask,
            "pixel_values": pixel_values,
            "time_ids": time_ids,
            "tokenizer_one_text_input_ids": tokenzier_one_text_input_ids,
            "tokenizer_two_text_input_ids": tokenzier_two_text_input_ids,
        }

    def select_only_images_greater_than(d):
        width, height = d["image"].size

        return width >= min_resolution and height >= min_resolution

    pipeline = [
        wds.ResampledShards(wds_dataset_url),
        tarfile_to_samples_nothrow,
        wds.shuffle(shuffle_buffer_size),
        wds.decode("pil", handler=wds.ignore_and_continue),
        wds.rename(
            image="jpg;png;jpeg;webp",
            text="text;txt;caption",
            metadata="json",
            handler=wds.warn_and_continue,
        ),
        *([] if min_resolution is None else [wds.select(select_only_images_greater_than)]),
        wds.map(mapper),
        wds.batched(per_gpu_batch_size, partial=False, collation_fn=default_collate),
    ]

    dataset = wds.DataPipeline(*pipeline)

    return dataset


def log_validation(pretrained_model_name_or_path, unet, vae, accelerator, weight_dtype):
    pipeline = StableDiffusionXLInpaintPipeline.from_pretrained(
        pretrained_model_name_or_path,
        vae=accelerator.unwrap_model(vae),
        unet=accelerator.unwrap_model(unet),
        torch_dtype=weight_dtype,
    )

    pipeline = pipeline.to(accelerator.device)
    pipeline.set_progress_bar_config(disable=True)

    # run inference
    generator = torch.Generator(device=accelerator.device).manual_seed(args.seed) if args.seed else None

    with torch.cuda.amp.autocast():
        validation_dirs = os.listdir("./validation")
        for validation_dir in validation_dirs:
            prompt = validation_dir
            image = Image.open("./validation/" + validation_dir + "/image.png")
            mask_image = Image.open("./validation/" + validation_dir + "/mask.png")
            images = pipeline(
                prompt=prompt,
                image=image,
                mask_image=mask_image,
                num_images_per_prompt=4,
                strength=0.8,
                generator=generator,
            ).images

    wandb.log({"validation": [wandb.Image(image) for image in images]})

    del pipeline
    torch.cuda.empty_cache()


def main(args):
    logging_dir = Path(args.output_dir, args.logging_dir)

    accelerator_project_config = ProjectConfiguration(project_dir=args.output_dir, logging_dir=logging_dir)

    accelerator = Accelerator(
        gradient_accumulation_steps=args.gradient_accumulation_steps,
        mixed_precision=args.mixed_precision,
        log_with=args.report_to,
        project_config=accelerator_project_config,
    )

    # Make one log on every process with the configuration for debugging.
    logging.basicConfig(
        format="%(asctime)s - %(levelname)s - %(name)s - %(message)s",
        datefmt="%m/%d/%Y %H:%M:%S",
        level=logging.INFO,
    )
    logger.info(accelerator.state, main_process_only=False)
    if accelerator.is_local_main_process:
        datasets.utils.logging.set_verbosity_warning()
        transformers.utils.logging.set_verbosity_warning()
        diffusers.utils.logging.set_verbosity_info()
    else:
        datasets.utils.logging.set_verbosity_error()
        transformers.utils.logging.set_verbosity_error()
        diffusers.utils.logging.set_verbosity_error()

    # If passed along, set the training seed now.
    if args.seed is not None:
        set_seed(args.seed)

    # Handle the repository creation
    if accelerator.is_main_process:
        if args.output_dir is not None:
            os.makedirs(args.output_dir, exist_ok=True)

        if args.push_to_hub:
            repo_id = create_repo(
                repo_id=args.hub_model_id or Path(args.output_dir).name, exist_ok=True, token=args.hub_token
            ).repo_id

    # Load the tokenizers
    tokenizer_one = AutoTokenizer.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="tokenizer", revision=args.revision, use_fast=False
    )
    tokenizer_two = AutoTokenizer.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="tokenizer_2", revision=args.revision, use_fast=False
    )

    # import correct text encoder classes
    text_encoder_cls_one = import_model_class_from_model_name_or_path(
        args.pretrained_model_name_or_path, args.revision
    )
    text_encoder_cls_two = import_model_class_from_model_name_or_path(
        args.pretrained_model_name_or_path, args.revision, subfolder="text_encoder_2"
    )

    text_encoder_one = text_encoder_cls_one.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="text_encoder", revision=args.revision
    )
    text_encoder_two = text_encoder_cls_two.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="text_encoder_2", revision=args.revision
    )
    vae_path = (
        args.pretrained_model_name_or_path
        if args.pretrained_vae_model_name_or_path is None
        else args.pretrained_vae_model_name_or_path
    )
    vae = AutoencoderKL.from_pretrained(
        vae_path, subfolder="vae" if args.pretrained_vae_model_name_or_path is None else None, revision=args.revision
    )
    unet = UNet2DConditionModel.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="unet", revision=args.revision
    )
    noise_scheduler = EulerDiscreteScheduler.from_pretrained(args.pretrained_model_name_or_path, subfolder="scheduler")

    with torch.no_grad():
        # Increase the number of input channels in the unet to handle
        # the additional mask and masked image conditioning
        orig_in_channels = unet.config.in_channels
        unet.config.in_channels = 2 * orig_in_channels + 1  # 2 images + 1 mask
        original_conv_in = unet.conv_in
        unet.conv_in = nn.Conv2d(
            unet.config.in_channels, unet.config.block_out_channels[0], kernel_size=3, padding=(1, 1)
        )
        unet.conv_in.bias = original_conv_in.bias
        # set first `origin_n_channels` input channels of `unet.conv_in.weight` to `original_conv_in.weight`
        # 2d conv weight shape: `out channels, in channels, kernel height, kernel width`
        unet.conv_in.weight[:, :orig_in_channels, :, :] = original_conv_in.weight
        unet.conv_in.weight[:, orig_in_channels:, :, :] = 0
        del original_conv_in

    # Freeze vae and text encoders.
    vae.requires_grad_(False)
    text_encoder_one.requires_grad_(False)
    text_encoder_two.requires_grad_(False)

    # For mixed precision training we cast all non-trainable weigths to half-precision
    # as these weights are only used for inference, keeping weights in full precision is not required.
    weight_dtype = torch.float32
    if accelerator.mixed_precision == "fp16":
        weight_dtype = torch.float16
    elif accelerator.mixed_precision == "bf16":
        weight_dtype = torch.bfloat16

    # Move unet, vae and text_encoder to device and cast to weight_dtype
    # The VAE is in float32 to avoid NaN losses.
    vae.to(accelerator.device, dtype=torch.float32)
    text_encoder_one.to(accelerator.device, dtype=weight_dtype)
    text_encoder_two.to(accelerator.device, dtype=weight_dtype)

    # Create EMA for the unet.
    if args.use_ema:
        ema_unet = UNet2DConditionModel.from_pretrained(
            args.pretrained_model_name_or_path, subfolder="unet", revision=args.revision
        )
        ema_unet = EMAModel(ema_unet.parameters(), model_cls=UNet2DConditionModel, model_config=ema_unet.config)

    if args.enable_xformers_memory_efficient_attention:
        if is_xformers_available():
            import xformers

            xformers_version = version.parse(xformers.__version__)
            if xformers_version == version.parse("0.0.16"):
                logger.warn(
                    "xFormers 0.0.16 cannot be used for training in some GPUs. If you observe problems during training, please update xFormers to at least 0.0.17. See https://huggingface.co/docs/diffusers/main/en/optimization/xformers for more details."
                )
            unet.enable_xformers_memory_efficient_attention()
        else:
            raise ValueError("xformers is not available. Make sure it is installed correctly")

    # `accelerate` 0.16.0 will have better support for customized saving
    if version.parse(accelerate.__version__) >= version.parse("0.16.0"):
        # create custom saving & loading hooks so that `accelerator.save_state(...)` serializes in a nice format
        def save_model_hook(models, weights, output_dir):
            if args.use_ema:
                ema_unet.save_pretrained(os.path.join(output_dir, "unet_ema"))

            for i, model in enumerate(models):
                model.save_pretrained(os.path.join(output_dir, "unet"))

                # make sure to pop weight so that corresponding model is not saved again
                weights.pop()

        def load_model_hook(models, input_dir):
            if args.use_ema:
                load_model = EMAModel.from_pretrained(os.path.join(input_dir, "unet_ema"), UNet2DConditionModel)
                ema_unet.load_state_dict(load_model.state_dict())
                ema_unet.to(accelerator.device)
                del load_model

            for i in range(len(models)):
                # pop models so that they are not loaded again
                model = models.pop()

                # load diffusers style into model
                load_model = UNet2DConditionModel.from_pretrained(input_dir, subfolder="unet")
                model.register_to_config(**load_model.config)

                model.load_state_dict(load_model.state_dict())
                del load_model

        accelerator.register_save_state_pre_hook(save_model_hook)
        accelerator.register_load_state_pre_hook(load_model_hook)

    if args.gradient_checkpointing:
        unet.enable_gradient_checkpointing()

    # Enable TF32 for faster training on Ampere GPUs,
    # cf https://pytorch.org/docs/stable/notes/cuda.html#tensorfloat-32-tf32-on-ampere-devices
    if args.allow_tf32:
        torch.backends.cuda.matmul.allow_tf32 = True

    if args.scale_lr:
        args.learning_rate = (
            args.learning_rate * args.gradient_accumulation_steps * args.train_batch_size * accelerator.num_processes
        )

    # Use 8-bit Adam for lower memory usage or to fine-tune the model in 16GB GPUs
    if args.use_8bit_adam:
        try:
            import bitsandbytes as bnb
        except ImportError:
            raise ImportError(
                "To use 8-bit Adam, please install the bitsandbytes library: `pip install bitsandbytes`."
            )

        optimizer_class = bnb.optim.AdamW8bit
    else:
        optimizer_class = torch.optim.AdamW

    # Optimizer creation
    params_to_optimize = unet.parameters()
    optimizer = optimizer_class(
        params_to_optimize,
        lr=args.learning_rate,
        betas=(args.adam_beta1, args.adam_beta2),
        weight_decay=args.adam_weight_decay,
        eps=args.adam_epsilon,
    )

    train_dataset = make_wds_inpainting_dataset(
        wds_dataset_url=args.wds_dataset_url,
        tokenizer_one=tokenizer_one,
        tokenizer_two=tokenizer_two,
        proportion_empty_prompts=args.proportion_empty_prompts,
        per_gpu_batch_size=args.train_batch_size,
        resolution=args.resolution,
        min_resolution=args.min_resolution,
    )

    train_dataloader = torch.utils.data.DataLoader(
        train_dataset,
        shuffle=False,
        batch_size=None,
        num_workers=args.dataloader_num_workers,
        pin_memory=True,
        persistent_workers=True,
    )

    lr_scheduler = get_scheduler(
        args.lr_scheduler,
        optimizer=optimizer,
        num_warmup_steps=args.lr_warmup_steps * args.gradient_accumulation_steps,
        num_training_steps=args.max_train_steps * args.gradient_accumulation_steps,
    )

    # Prepare everything with our `accelerator`.
    unet, optimizer, lr_scheduler = accelerator.prepare(unet, optimizer, lr_scheduler)

    # We need to initialize the trackers we use, and also store our configuration.
    # The trackers initializes automatically on the main process.
    if accelerator.is_main_process:
        accelerator.init_trackers("text2image-fine-tune-sdxl", config=vars(args))

    # Train!
    total_batch_size = args.train_batch_size * accelerator.num_processes * args.gradient_accumulation_steps

    logger.info("***** Running training *****")
    logger.info(f"  Instantaneous batch size per device = {args.train_batch_size}")
    logger.info(f"  Total train batch size (w. parallel, distributed & accumulation) = {total_batch_size}")
    logger.info(f"  Gradient Accumulation steps = {args.gradient_accumulation_steps}")
    logger.info(f"  Total optimization steps = {args.max_train_steps}")
    global_step = 0

    # Potentially load in the weights and states from a previous save
    if args.resume_from_checkpoint:
        if args.resume_from_checkpoint != "latest":
            path = os.path.basename(args.resume_from_checkpoint)
        else:
            # Get the most recent checkpoint
            dirs = os.listdir(args.output_dir)
            dirs = [d for d in dirs if d.startswith("checkpoint")]
            dirs = sorted(dirs, key=lambda x: int(x.split("-")[1]))
            path = dirs[-1] if len(dirs) > 0 else None

        if path is None:
            accelerator.print(
                f"Checkpoint '{args.resume_from_checkpoint}' does not exist. Starting a new training run."
            )
            args.resume_from_checkpoint = None
        else:
            accelerator.print(f"Resuming from checkpoint {path}")
            accelerator.load_state(os.path.join(args.output_dir, path))
            global_step = int(path.split("-")[1])

    # Only show the progress bar once on each machine.
    progress_bar = tqdm(range(global_step, args.max_train_steps), disable=not accelerator.is_local_main_process)
    progress_bar.set_description("Steps")

    for epoch in range(0, 1):
        unet.train()
        train_loss = 0.0
        for step, batch in enumerate(train_dataloader):
            with torch.no_grad():
                tokenizer_one_text_input_ids = batch["tokenizer_one_text_input_ids"].to(accelerator.device)
                tokenizer_two_text_input_ids = batch["tokenizer_two_text_input_ids"].to(accelerator.device)

                pixel_values = batch["pixel_values"].to(accelerator.device, dtype=vae.dtype)

                mask = batch["mask"].to(accelerator.device)

                masked_pixel_values = (pixel_values * 2 - 1) * (mask < 0.5)

                time_ids = batch["time_ids"].to(accelerator.device, dtype=weight_dtype)

                bsz = pixel_values.shape[0]

                prompt_embeds_one = text_encoder_one(
                    tokenizer_one_text_input_ids,
                    output_hidden_states=True,
                )
                prompt_embeds_one = prompt_embeds_one.hidden_states[-2]

                prompt_embeds_two = text_encoder_two(
                    tokenizer_two_text_input_ids,
                    output_hidden_states=True,
                )
                pooled_prompt_embeds = prompt_embeds_two[0]
                prompt_embeds_two = prompt_embeds_two.hidden_states[-2]

                pooled_prompt_embeds = pooled_prompt_embeds.view(bsz, -1)

                prompt_embeds = torch.concat((prompt_embeds_one, prompt_embeds_two), dim=-1)

                encoded_pixel_values = vae.encode(pixel_values).latent_dist.sample()
                encoded_pixel_values = encoded_pixel_values * vae.config.scaling_factor

                encoded_masked_pixel_values = vae.encode(masked_pixel_values).latent_dist.sample()
                encoded_masked_pixel_values = encoded_masked_pixel_values * vae.config.scaling_factor

                timesteps = torch.randint(
                    0, noise_scheduler.config.num_train_timesteps, (bsz,), device=accelerator.device
                )
                timesteps = timesteps.long()

                schedule_timesteps = noise_scheduler.timesteps.to(accelerator.device, dtype=torch.float32)
                step_indices = [(schedule_timesteps == t).nonzero().item() for t in timesteps]
                sigmas = noise_scheduler.sigmas.to(device=accelerator.device, dtype=weight_dtype)
                sigma = sigmas[step_indices].flatten()
                loss_weight = sigma**-2.0

                noise = torch.randn_like(encoded_masked_pixel_values)

                noisy_encoded_pixel_values = noise_scheduler.add_noise(encoded_pixel_values, noise, timesteps)
                noisy_encoded_pixel_values = noisy_encoded_pixel_values / (
                    (sigma[:, None, None, None] ** 2 + 1) ** 0.5
                )

                vae_scale_factor = 2 ** (len(vae.config.block_out_channels) - 1)
                latent_dimension = args.resolution // vae_scale_factor
                mask_resized_to_latent_dims = F.interpolate(
                    mask.to(torch.float32), size=(latent_dimension, latent_dimension)
                )

                model_input = torch.cat(
                    [noisy_encoded_pixel_values, mask_resized_to_latent_dims, encoded_masked_pixel_values],
                    dim=1,
                )

            with accelerator.accumulate(unet):
                model_pred = unet(
                    model_input,
                    timesteps,
                    prompt_embeds,
                    added_cond_kwargs={"time_ids": time_ids, "text_embeds": pooled_prompt_embeds},
                ).sample

                squared_errors = (model_pred.float() - noise.float()) ** 2
                batched_mean_squared_errors = squared_errors.reshape(bsz, -1).mean(-1)
                weighted_losses = loss_weight * batched_mean_squared_errors
                loss = weighted_losses.mean()

                # Gather the losses across all processes for logging (if we use distributed training).
                avg_loss = accelerator.gather(loss.repeat(args.train_batch_size)).mean()
                train_loss += avg_loss.item() / args.gradient_accumulation_steps

                # Backpropagate
                accelerator.backward(loss)
                if accelerator.sync_gradients:
                    params_to_clip = unet.parameters()
                    accelerator.clip_grad_norm_(params_to_clip, args.max_grad_norm)
                optimizer.step()
                lr_scheduler.step()
                optimizer.zero_grad()

            # Checks if the accelerator has performed an optimization step behind the scenes
            if accelerator.sync_gradients:
                progress_bar.update(1)
                global_step += 1
                accelerator.log({"train_loss": train_loss}, step=global_step)
                train_loss = 0.0

                if accelerator.is_main_process:
                    if global_step % args.checkpointing_steps == 0:
                        # _before_ saving state, check if this save would set us over the `checkpoints_total_limit`
                        if args.checkpoints_total_limit is not None:
                            checkpoints = os.listdir(args.output_dir)
                            checkpoints = [d for d in checkpoints if d.startswith("checkpoint")]
                            checkpoints = sorted(checkpoints, key=lambda x: int(x.split("-")[1]))

                            # before we save the new checkpoint, we need to have at _most_ `checkpoints_total_limit - 1` checkpoints
                            if len(checkpoints) >= args.checkpoints_total_limit:
                                num_to_remove = len(checkpoints) - args.checkpoints_total_limit + 1
                                removing_checkpoints = checkpoints[0:num_to_remove]

                                logger.info(
                                    f"{len(checkpoints)} checkpoints already exist, removing {len(removing_checkpoints)} checkpoints"
                                )
                                logger.info(f"removing checkpoints: {', '.join(removing_checkpoints)}")

                                for removing_checkpoint in removing_checkpoints:
                                    removing_checkpoint = os.path.join(args.output_dir, removing_checkpoint)
                                    shutil.rmtree(removing_checkpoint)

                        save_path = os.path.join(args.output_dir, f"checkpoint-{global_step}")
                        accelerator.save_state(save_path)
                        logger.info(f"Saved state to {save_path}")

            logs = {"step_loss": loss.detach().item(), "lr": lr_scheduler.get_last_lr()[0]}
            progress_bar.set_postfix(**logs)

            if global_step >= args.max_train_steps:
                break

            if accelerator.is_main_process:
                if global_step % args.validation_steps == 0:
                    log_validation(args.pretrained_model_name_or_path, unet, vae, accelerator, weight_dtype)

    accelerator.wait_for_everyone()
    if accelerator.is_main_process:
        unet = accelerator.unwrap_model(unet)
        if args.use_ema:
            ema_unet.copy_to(unet.parameters())

        # Serialize pipeline.
        vae = AutoencoderKL.from_pretrained(
            vae_path,
            subfolder="vae" if args.pretrained_vae_model_name_or_path is None else None,
            revision=args.revision,
            torch_dtype=weight_dtype,
        )
        pipeline = StableDiffusionXLPipeline.from_pretrained(
            args.pretrained_model_name_or_path, unet=unet, vae=vae, revision=args.revision, torch_dtype=weight_dtype
        )
        if args.prediction_type is not None:
            scheduler_args = {"prediction_type": args.prediction_type}
            pipeline.scheduler = pipeline.scheduler.from_config(pipeline.scheduler.config, **scheduler_args)
        pipeline.save_pretrained(args.output_dir)

        if args.push_to_hub:
            save_model_card(
                repo_id=repo_id,
                base_model=args.pretrained_model_name_or_path,
                dataset_name=args.dataset_name,
                repo_folder=args.output_dir,
                vae_path=args.pretrained_vae_model_name_or_path,
            )
            upload_folder(
                repo_id=repo_id,
                folder_path=args.output_dir,
                commit_message="End of training",
                ignore_patterns=["step_*", "epoch_*"],
            )

    accelerator.end_training()


if __name__ == "__main__":
    args = parse_args()
    main(args)
